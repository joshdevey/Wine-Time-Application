package uk.ac.mmu.advprog.assessment.browser;

public class Wine {
    public String name;
    public String type;
    public String Winery;
    public String Country;
    public String ABV;

    public Wine(String name, String type, String winery, String country, String ABV) {
        this.name = name;
        this.type = type;
        Winery = winery;
        Country = country;
        this.ABV = ABV;
    }
}

